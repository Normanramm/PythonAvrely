// Запрашиваем имя у пользователя
let имяПользователя = prompt("Как тебя зовут?");

// Проверяем, ввел ли пользователь имя
if (имяПользователя) {
    // Показываем приветствие
    alert("Привет, " + имяПользователя + "! Рад познакомиться!");
    console.log("Пользователь ввел имя: " + имяПользователя);
} else {
    alert("Ты не ввел имя :(");
    console.log("Пользователь не ввел имя");
}

// Спрашиваем любимое число
let любимоеЧисло = prompt("Какое твое любимое число?");
любимоеЧисло = Number(любимоеЧисло); // Превращаем текст в число

if (!isNaN(любимоеЧисло)) { // Проверяем, точно ли это число
    alert("Твое число умноженное на 2: " + (любимоеЧисло * 2));
} else {
    alert("Это не число!");
}