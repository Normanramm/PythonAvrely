import qrcode
import qrcode.image.svg  # Модуль для SVG-вывода


"""Например, если нужно распечатать QR-код на визитке, баннере или плакате, 
важно, чтобы изображение не теряло качество при увеличении.
Для этого используется формат SVG — это векторная графика, 
которая масштабируется без потери качества.
Чтобы создать QR-код в формате SVG, нам нужно установить специальную версию библиотеки 
qrcode с поддержкой SVG. Откройте терминал (или командную строку) и введите:pip install qrcode[svg]

qrcode.image.svg — модуль внутри библиотеки qrcode, который умеет работать с векторной графикой.

SvgImage — класс-фабрика, который создаёт обычные SVG-изображения (без лишних элементов).

qrcode.make() — простая функция для создания QR-кода. Мы передаём ей:данные (ссылку или текст),

и image_factory=factory, чтобы результат был именно в SVG-формате.
"""


# Получаем данные от пользователя
data = input("Введите ссылку или текст: ")

# Указываем SVG-фабрику
factory = qrcode.image.svg.SvgImage

# Создаём QR-код с SVG-выводом
img = qrcode.make(data, image_factory=factory)

# Сохраняем изображение в файл
img.save("D:\\Downloaded files Brauser\\qr_code.svg")

# Уведомление об успехе
print("✅ SVG-файл успешно сохранён!")
