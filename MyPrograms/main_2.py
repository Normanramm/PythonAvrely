import random

def number_guessing_game():
    # –°–æ–∑–¥–∞–µ–º —Å–ø–∏—Å–æ–∫ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª (–º–æ–∂–Ω–æ –∏–∑–º–µ–Ω–∏—Ç—å –¥–∏–∞–ø–∞–∑–æ–Ω –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ)
    numbers = list(range(1, 10))  # –ß–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 9
    random.shuffle(numbers)
    score = 0
    
    print("‚ú® –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∏–≥—Ä—É '–£–≥–∞–¥–∞–π —á–∏—Å–ª–æ'!")
    print("–Ø –∑–∞–≥–∞–¥–∞–ª —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 9, –ø–æ–ø—Ä–æ–±—É–π —É–≥–∞–¥–∞—Ç—å –∏—Ö –ø–æ –ø–æ—Ä—è–¥–∫—É!")
    
    while numbers:
        current_num = numbers.pop()  # –ë–µ—Ä–µ–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ —á–∏—Å–ª–æ –∏–∑ —Å–ø–∏—Å–∫–∞
        print(f"\n–û—Å—Ç–∞–ª–æ—Å—å —á–∏—Å–µ–ª: {len(numbers)}")
        
        try:
            guess = int(input("–¢–≤–æ–π –≤–∞—Ä–∏–∞–Ω—Ç: "))
        except ValueError:
            print("‚ö†Ô∏è –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–æ–¥–∏ —Ç–æ–ª—å–∫–æ —á–∏—Å–ª–∞!")
            continue
            
        if guess == current_num:
            score += 1
            print(f"‚úÖ –í–µ—Ä–Ω–æ! –≠—Ç–æ –±—ã–ª–æ —á–∏—Å–ª–æ {current_num}")
        else:
            print(f"‚ùå –ù–µ —É–≥–∞–¥–∞–ª! –≠—Ç–æ –±—ã–ª–æ —á–∏—Å–ª–æ {current_num}")
        
        print(f"–¢–µ–∫—É—â–∏–π —Å—á–µ—Ç: {score}/{len(range(1, 10)) - len(numbers)}")
    
    print(f"\nüéÆ –ò–≥—Ä–∞ –æ–∫–æ–Ω—á–µ–Ω–∞! –¢–≤–æ–π —Ñ–∏–Ω–∞–ª—å–Ω—ã–π —Å—á–µ—Ç: {score}/9")

# –ó–∞–ø—É—Å–∫ –∏–≥—Ä—ã
if __name__ == "__main__":
    number_guessing_game()





